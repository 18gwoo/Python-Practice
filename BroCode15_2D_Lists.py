# 2D lists/multi dimmensional lists: a list of lists

drinks = [ "coffee", "soda", "tea"]
dinner = ["Pizza","hamburger","hotdog"]
dessert = ["cake", "ice cream"]

food_and_drinks = [drinks, dinner, dessert]

#first bracket denotes which list and second denotes what element within each list
print(food_and_drinks)
print(food_and_drinks[0][0])
print(food_and_drinks[0][1])
print(food_and_drinks[1][2])
